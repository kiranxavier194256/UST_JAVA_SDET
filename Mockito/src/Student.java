
public class Student {
	private String name;
	private int age;
	public Student(String s,int x){
		name = s;
		age = x;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	

}
